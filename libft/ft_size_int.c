int		ft_size_int(int n)
{
	int i;

	i = 0;
	if ((n > -2147483648) && (n <= 2147483647))
	{
		if (n < 0)
		{
			n = n * (-1);
			i++;
		}
		while (n > 9)
		{
			n = n / 10;
			i++;
		}
		if (n < 10)
			i++;
	}
	if (n == -2147483648)
		return (11);
	if (n == 0)
		return (1);
	return (i);
}
